from flask import Flask, request

app = Flask(__name__)

# এখানে আপনি যত খুশি প্রশ্ন এবং উত্তর যোগ করতে পারবেন
responses = {
    "কেমন আছো?": "আমি ভালো আছি, জহিরুল ইসলাম ভাই আমাকে তৈরি করেছেন। আপনাকে কীভাবে সাহায্য করতে পারি?",
    "তোমার নাম কি?": "আমি জহিরুল আই (Zahirul AI)।",
    "তোমাকে কে তৈরি করেছে?": "আমাকে তৈরি করেছেন জহিরুল ইসলাম। তিনি একজন অ্যান্ড্রয়েড অ্যাপ ডেভেলপার এবং পাইথন বিশেষজ্ঞ।",
    "কি খবর?": "সব ভালো! আপনার দিনটি কেমন কাটছে?",
    "ধন্যবাদ": "আপনাকেও অনেক ধন্যবাদ! জহিরুল ইসলামের পক্ষ থেকে শুভকামনা।"
}

@app.route('/')
def home():
    # অ্যাপ থেকে আসা প্রশ্ন ধরবে (যেমন: ?text=কেমন আছো)
    user_query = request.args.get('text')
    
    if not user_query:
        return "Zahirul AI API is Running..."

    # উত্তর খোঁজা
    answer = responses.get(user_query, "দুঃখিত, আমি এই প্রশ্নের উত্তর জানি না। তবে জহিরুল ইসলাম ভাই আমাকে আরও নতুন কিছু শেখাচ্ছেন!")
    return answer

if __name__ == "__main__":
    app.run(host='0.0.0.0', port=5000)
  
